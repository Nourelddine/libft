#include "libft.h"

int		ft_intlen(int c)
{
	int s;
	int i;
	int zeros;

	zeros = 10;
	i = 0;
	s = 1000000000;
	while (s)
	{
		if (c >= s)
			return (zeros - i);
		s /= 10;
		i++;
	}
	return (0);
}
